abstract class A1 
{
	abstract void test1();
	public static void main(String[] args) 
	{
		System.out.println("Hello World!");
	}
}

/*
if abstract class contain the main method then we can able to run that
abstract class.

object creating is not required, as because static method are there
which doesn't require to create object.

if main method is not static we need to every time to create object.
this is very costly as well as lengthy process.
and it has to all the super class as well as specify which constructor we need to use.
*/